def _norm(name: str) -> str:
    s = (name or "").strip().lower()
    s = s.replace("ё", "е")
    s = re.sub(r"\s+", " ", s)
    return s


def canon_city(name: str) -> str:
    s = _norm(name)
    if s in LATIN:
        s = LATIN[s]
    return ALIASES.get(s, s)


def zone(name: str) -> str:
    """Вернёт 'ВВЛ', 'МВЛ' или '?' (не распознано)."""
    c = canon_city(name)
    if not c or re.search(r"\d{4}-|^\d+$", c):
        return "?"
    if c in RU_CITIES:
        return "ВВЛ"
    if c in INTL_CITIES:
        return "МВЛ"
    return "?"
